public class PrintChar {
    public static void main(String args[]){
        System.out.println("A to Z");

        char a = 'A';
        while(a<='Z'){
            System.out.print(a+" ");
            a++;

        }
        System.out.println();
        char b = 'a';
        while(b<='z'){
            System.out.print(b+" ");
            b++;

        }
    }
}
