public class ForLoop {
    public static void main(String args[]){
        
        for(int i=1111;i<=1121;i++){
            System.out.print(i+" ");
    }
    System.out.println();

    for(int i=1234;i>=1223;i--){
        System.out.print(i+" ");
    }
     System.out.println();

    for(int i=-1111;i>=-1121;i--){
        System.out.print(i+" ");
    }
    System.out.println();

    for(int i=-12;i<=-1;i++){
        System.out.print(i+" ");
    }
}
}
