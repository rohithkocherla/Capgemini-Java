public class ForLoopChar {
    public static void main(String args[]){

        for(char ch='d';ch<='j';ch++){
        System.out.print(ch +" ");
    }
        System.out.println();

    for(char ch='m';ch>='e';ch--){
        System.out.print(ch +" ");
    }

     System.out.println();
        
    for(char ch='z';ch>='t';ch--){
        System.out.print(ch +" ");
    }

     System.out.println();
        
    for(char ch='r';ch<='x';ch++){
        System.out.print(ch +" ");
    }
}
    
}
