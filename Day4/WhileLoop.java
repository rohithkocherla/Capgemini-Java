public class WhileLoop {
    public static void main(String args[]){
        int a = -123;
        while(a>=-129){
            System.out.print(a+" ");
            a--;
        }
        System.out.println();
        int b=79;
        while(b<=84){
            System.out.print(b+" ");
            b++;
        }
        System.out.println();
        int c=-110;
        while(c<=-100){
            System.out.print(c+" ");
            c++;
        }
        System.out.println();
        int d=0;
        while(d>=-9){
            System.out.print(d+" ");
            d--;
        }
        
        
    }
}
